#### a.
Completions\
$\bar{x} = \frac{\sum x_i}{n} = \frac{350}{16} = 21.25$

$S_x = \sqrt{\frac{\sum (x_i - \bar{x})^2}{n - 1}} = 2.316$

Yards per pass\
$\bar{y} = \frac{\sum y_i}{n} = \frac{207.8}{16} = 12.99$

$S_y = \sqrt{\frac{\sum (y_i - \bar{y})^2}{n - 1}} = 2.52$
